// Write a Java program to declare an int variable x = 5 and test these 
// increment and decrement statements: 

public class main22 {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(++x);
        System.out.println(x--);
        System.out.println(x);
        System.out.println(--x);
        System.out.println(x++);
        System.out.println(x);
    }
}